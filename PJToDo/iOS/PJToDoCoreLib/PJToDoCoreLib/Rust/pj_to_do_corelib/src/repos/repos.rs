extern crate serde_derive;
extern crate serde;
extern crate serde_json;

use crate::common::pj_serialize::PJSerdeDeserialize;

#[derive(Serialize, Deserialize, Debug, Default)]
pub struct Repos {
    id: i64,
    node_id: String,
    name: String,
    full_name: String,
    html_url: String,
    description: String,
    url: String,
    contents_url: String,
    downloads_url: String,
}

impl<'b> PJSerdeDeserialize<'b> for Repos {
    type Item = Repos;
    fn new() -> Self::Item {
        Self::Item::default()
    }
}

#[derive(Serialize, Deserialize, Debug, Default)]
pub struct ReposRequestBody<'a> {
    pub name: &'a str,
    pub description: &'a str,
    pub homepage: &'a str,
    pub private: bool,
    pub has_issues: bool,
    pub has_projects: bool,
    pub has_wiki: bool,
}

impl<'a, 'b: 'a> PJSerdeDeserialize<'b> for ReposRequestBody<'a> {
    type Item = ReposRequestBody<'a>;
    fn new() -> Self::Item {
        Self::Item::default()
    }
}
